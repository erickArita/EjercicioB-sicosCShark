namespace EjerciciosBasicos;
//Un programa que muestre los números del 1 al 100 en la consola. (for)

public class Ejercicio6
{
    public Ejercicio6()
    {
        Console.WriteLine("Mostrar los números del 1 al 100");

        for (int i = 0; i < 100; i++)
        {
            Console.WriteLine(i + 1);
        }
    }
}